// Hook Installer — Automatically configures Claude Code hooks
//
// Claude Code hook format:
// {
//   "hooks": {
//     "PreToolUse": [
//       {
//         "matcher": "",            // empty = match all tools
//         "hooks": [
//           {
//             "type": "command",
//             "command": "echo hello"
//           }
//         ]
//       }
//     ]
//   }
// }

const fs = require('fs');
const path = require('path');
const os = require('os');

const CLAUDE_DIR = path.join(os.homedir(), '.claude');
const SETTINGS_FILE = path.join(CLAUDE_DIR, 'settings.json');
const HELPER_SCRIPT = path.join(CLAUDE_DIR, 'traffic-light-hook.js');
const PORT_FILE = path.join(CLAUDE_DIR, 'traffic-light-port');

const MARKER = 'claude-traffic-light';

function generateHelperScript(port) {
  return `// Auto-generated by Claude Traffic Light widget
// This script is called by Claude Code hooks to notify the traffic light.
// No process tree walking — Claude Code's own hook payload already provides
// the session_id and cwd; we just forward them.

const http = require('http');
const event = process.argv[2] || 'Unknown';
const port = ${port};

// Read Claude Code's hook payload from stdin, then forward to traffic light.
let stdin = '';
process.stdin.on('data', (c) => { stdin += c; });
process.stdin.on('end', () => {
  let parsed = {};
  try { parsed = JSON.parse(stdin || '{}'); } catch {}

  // Claude Code provides these in every hook payload.
  // Fall back to process.ppid (the node running Claude) if not present.
  const tool = parsed.tool_name || parsed.tool || '';
  const cwd = parsed.cwd || parsed.working_directory || '';
  const sessionId = parsed.session_id || parsed.sessionId || '';
  const claudePid = process.ppid;

  const data = JSON.stringify({
    event,
    tool,
    cwd,
    sessionId,
    claudePid,
  });
  const req = http.request(
    {
      hostname: '127.0.0.1',
      port,
      path: '/hook',
      method: 'POST',
      headers: { 'Content-Type': 'application/json' },
      timeout: 3000,
    },
    () => process.exit(0)
  );
  req.on('error', () => process.exit(0));
  req.on('timeout', () => { req.destroy(); process.exit(0); });
  req.write(data);
  req.end();
});
`;
}

function readSettings() {
  try {
    if (fs.existsSync(SETTINGS_FILE)) {
      const raw = fs.readFileSync(SETTINGS_FILE, 'utf-8');
      return JSON.parse(raw);
    }
  } catch {
    // If settings are corrupted, start fresh
  }
  return {};
}

function writeSettings(settings) {
  if (!fs.existsSync(CLAUDE_DIR)) {
    fs.mkdirSync(CLAUDE_DIR, { recursive: true });
  }
  fs.writeFileSync(SETTINGS_FILE, JSON.stringify(settings, null, 2), 'utf-8');
}

function isOurMatcherGroup(entry) {
  // Identify our matcher group by checking if any hook references traffic-light-hook.js
  // This catches both _marker tagged entries and old-format entries without _marker
  return (
    typeof entry === 'object' &&
    Array.isArray(entry.hooks) &&
    entry.hooks.some((h) =>
      h._marker === MARKER ||
      (typeof h.command === 'string' && h.command.includes('traffic-light-hook.js'))
    )
  );
}


function generateHooks(port) {
  // Use raw path — JSON.stringify handles escaping automatically
  const helperPath = HELPER_SCRIPT;

  return {
    PreToolUse: [
      {
        matcher: '',
        hooks: [
          {
            _marker: MARKER,
            type: 'command',
            command: `node "${helperPath}" PreToolUse`,
          },
        ],
      },
    ],
    PostToolUse: [
      {
        matcher: '',
        hooks: [
          {
            _marker: MARKER,
            type: 'command',
            command: `node "${helperPath}" PostToolUse`,
          },
        ],
      },
    ],
    Stop: [
      {
        matcher: '',
        hooks: [
          {
            _marker: MARKER,
            type: 'command',
            command: `node "${helperPath}" Stop`,
          },
        ],
      },
    ],
  };
}

function mergeHooks(existingHooks, newHooks) {
  const merged = { ...existingHooks };

  for (const [eventType, newMatcherGroups] of Object.entries(newHooks)) {
    if (!merged[eventType]) {
      // No existing hooks for this event — add ours
      merged[eventType] = newMatcherGroups;
    } else {
      // Filter out our old matcher groups, keep user's other hooks
      const otherGroups = merged[eventType].filter(
        (group) => !isOurMatcherGroup(group)
      );
      merged[eventType] = [...otherGroups, ...newMatcherGroups];
    }
  }

  return merged;
}

function install(port) {
  try {
    // 1. Write helper script
    const script = generateHelperScript(port);
    if (!fs.existsSync(CLAUDE_DIR)) {
      fs.mkdirSync(CLAUDE_DIR, { recursive: true });
    }
    fs.writeFileSync(HELPER_SCRIPT, script, 'utf-8');

    // 2. Write port file (for reference)
    fs.writeFileSync(PORT_FILE, String(port), 'utf-8');

    // 3. Read existing settings
    const settings = readSettings();

    // 4. Generate and merge hooks
    const newHooks = generateHooks(port);
    settings.hooks = mergeHooks(settings.hooks || {}, newHooks);

    // 5. Write back
    writeSettings(settings);

    return { success: true, port, helperPath: HELPER_SCRIPT, settingsPath: SETTINGS_FILE };
  } catch (err) {
    return { success: false, error: err.message };
  }
}

function uninstall() {
  try {
    const settings = readSettings();
    if (settings.hooks) {
      for (const eventType of Object.keys(settings.hooks)) {
        settings.hooks[eventType] = settings.hooks[eventType].filter(
          (group) => !isOurMatcherGroup(group)
        );
        // Remove empty arrays
        if (settings.hooks[eventType].length === 0) {
          delete settings.hooks[eventType];
        }
      }
      // Remove empty hooks object
      if (Object.keys(settings.hooks).length === 0) {
        delete settings.hooks;
      }
    }
    writeSettings(settings);

    // Remove helper script and port file
    try { fs.unlinkSync(HELPER_SCRIPT); } catch { /* ignore */ }
    try { fs.unlinkSync(PORT_FILE); } catch { /* ignore */ }

    return { success: true };
  } catch (err) {
    return { success: false, error: err.message };
  }
}

function isInstalled() {
  if (!fs.existsSync(HELPER_SCRIPT)) return false;

  const settings = readSettings();
  if (!settings.hooks) return false;

  for (const eventType of ['PreToolUse', 'PostToolUse', 'Stop']) {
    const groups = settings.hooks[eventType];
    if (!groups || !groups.some((g) => isOurMatcherGroup(g))) {
      return false;
    }
  }
  return true;
}

module.exports = { install, uninstall, isInstalled };
